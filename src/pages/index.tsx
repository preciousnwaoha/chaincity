import Plate from '@/components/Plate'
import Head from 'next/head'
import Button from "@mui/material/Button"
import Box from "@mui/material/Box"
import { useDispatch } from 'react-redux'
import { gameActions } from '@/store/game-slice'
import { PLAYERS_DUM } from '@/utils/dummy-data'
import { LANDS, LAND_SETS } from '@/utils/monopoly-data'
import { useRouter } from 'next/router'

export default function Home() {
  const router = useRouter()
  const dispatch = useDispatch();

  const usedLands = LANDS.map((land, index) => {
    return {
      id: `land-${index + 1}`,
      type: land.type,
    setID: land.setID,
    color: land.color,
    name: land.name,
    rent: land.rent,
    mortgageFactor: land.mortgageFactor,
    unmortgageFactor: land.unmortgageFactor,
    mortgaged: false,
    price: land.price,
    maxHouses: land.maxHouses,
    maxHotels: land.maxHotels,
    houseRentFactor: land.houseRentFactor,
    image: land.image,
    startPos: land.startPos,
    endPos: land.endPos,
    owner: "",
    houses: 0,
    }
  
  })

  let gameStepSequence = [...usedLands.map((_, index) => `land-${index + 1}`)]

  let modifiedPlayers = PLAYERS_DUM.map((player, index) => {
    return {
      name: player.name,
      address: player.address,
      lands: player.lands,
      character: player.character,
      cash: 15000,
      turn: player.turn,
      trades: [],
      position: gameStepSequence[0],
      pendingRent: false,
      bankrupt: false,
    }
  })

  const handleStartGame = () => {
    dispatch(gameActions.setup({
      playing: true,
      lands: usedLands,
    gameStepSequence,
    players: modifiedPlayers,
    startingCash: 15000,
      logs: [
        {
          timestamp: new Date().getTime(),
          message: "Game Started",
        }
      ],
      bankCash: 100000,
      bankLands: [], 
      bankHoldings: [],
      turn: 0,
      landSets: LAND_SETS
    }))

    router.push("/game")
  }
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main >
        <Box>
          <Button onClick={handleStartGame}>PLAY</Button>
        </Box>
      </main>
    </>
  )
}
